<h2>Research</h2>

<p class="lede">
  Exploring how AI systems can be designed to remember appropriately, earn trust through transparency,
  and form sustainable long-term relationships with people.
</p>

<div class="section">
  <h3>Research Theme</h3>
  <p class="theme">Human-Centered AI Systems</p>
</div>

<div class="section">
  <h3>Focus Areas</h3>
  <div class="area">
    <h4>Memory</h4>
    <p>What should AI remember? How should memories be structured, retrieved, and forgotten? Investigating memory architectures that balance utility with privacy.</p>
  </div>
  <div class="area">
    <h4>Trust</h4>
    <p>How do users develop trust in AI systems that persist over time? What role does transparency play in earning and maintaining that trust?</p>
  </div>
  <div class="area">
    <h4>Transparency</h4>
    <p>Can showing users how an AI reasons, what it remembers, and when it acts increase appropriate trust and reduce over-reliance?</p>
  </div>
  <div class="area">
    <h4>Personal AI</h4>
    <p>What does it mean for an AI to be personal rather than generic? How do memory, continuity, and user control shape the experience?</p>
  </div>
</div>

<div class="section">
  <h3>Experiments</h3>
  <div class="experiment">
    <h4>Source Transparency</h4>
    <p>Investigating whether showing users the origin of an AI's knowledge affects their trust in its responses. What happens when users can see exactly where information came from?</p>
  </div>
  <div class="experiment">
    <h4>Memory Visibility</h4>
    <p>Exploring how revealing an AI's recalled memories before they influence responses changes user perception. Does seeing remembered context increase trust or cause over-reliance?</p>
  </div>
  <div class="experiment">
    <h4>Local vs Cloud AI</h4>
    <p>Comparing user trust in locally-executed versus cloud-based AI systems. Does ownership of execution change the relationship?</p>
  </div>
</div>

